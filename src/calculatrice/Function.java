package calculatrice;

/**
 *
 * @author kevin
 * Class which includs mathematics methods
 */
public class Function {
    
    /**
     *
     * @param a long
     * @param b long
     * @return long
     * Function which adds 2 numbers and return the result
     */
    public long addition ( long a , long b ){
        return a + b;
    }
    
    /**
     *
     * @param a long
     * @param b long
     * @return long
     * Function which multiply 2 numbers and return the result
     */
    public long multiplication ( long a , long b ){
        return a * b;
    }
}
